//0.1秒讀一次感測器資料
void TIM2_Init(void)
{
	RCC->APB1ENR1 |= 1 << 0;  //設定TIM2外設時鐘
	TIM2->SMCR    |= 000 << 0;//設定為內部時鐘

	TIM2->ARR     = 8000-1; //設定產生中斷目標值
	TIM2->PSC     = 1000-1;  //設定預分頻器
	TIM2->CR1     = 1 << 7;  //設定ARPE，允許自動裝載
	TIM2->CR1    |= 0 << 4;  //設定DIR，上數模式
	TIM2->CR1    |= 1 << 0;  //設定CEN，始能計數器
	TIM2->EGR     = 1 << 0;  //設定UG，初始化計數器，並立刻產生中斷
	TIM2->SR      = 0 << 0;  //設定UIF，清除中斷

	TIM2->DIER   |= 1 << 0 ; //設定UIE，允許更新中斷

	NVIC->ISER[0] = 1 << 28; //TIM2的中斷位置在28

	HAL_NVIC_SetPriority(TIM2_IRQn, 8, 0);
	HAL_NVIC_EnableIRQ(TIM2_IRQn);
}

void TIM2_IRQHandler(void)
{
	if( TIM2->SR & 0x0001)     //再次確認是否有中斷請求
	{
	    osSemaphoreRelease(mySemHandle);  // 發出信號
		TIM2->SR &= ~(1 << 0);//手動清除中斷，等待下次中斷清求
	}
}





