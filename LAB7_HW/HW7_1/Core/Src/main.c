void MoveAverage_single(int16_t *input, int16_t *output, uint8_t AVG_WINDOW,uint8_t *count,int16_t *last,uint8_t *ptr, int16_t *sum)
{

    *sum += *input;
	if ((*count) >= AVG_WINDOW) {
		if(*ptr == AVG_WINDOW) *ptr = 0;

		*sum -= last[*ptr];
		*output = *sum / AVG_WINDOW;
		last[*ptr] = *input;
		(*ptr)++;

	} else {
		*output = *sum / (*count + 1);
		(*count)++;
		last[*ptr] = *input;
		(*ptr)++;
	}

}
